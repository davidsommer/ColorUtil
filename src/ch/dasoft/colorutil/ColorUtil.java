  public static String convertRgbToHex(Color color) {
    String hex = Integer.toHexString(color.getRGB() & 0xffffff);
    if (hex.length() < 6) {
        hex = "0" + hex;
    }
    return "#" + hex;
  }

  public static Color convertHexToColor(String hex) {
    return new Color(Integer.parseInt(hex, 16));
  }

  public static boolean isHell(Color c) {

    double rot = (double)c.getRed() / 255;
    double gruen = (double)c.getGreen() / 255;
    double blau = (double)c.getBlue() / 255;

    double helligkeit = (0.2126 * rot) + (0.7152 * gruen) + (0.0722 * blau);

    return helligkeit * 255 > 127;

  }

